#!/bin/bash

echo "🧪 Тест 1: Проверка без аргументов"
if ./DO; then
    echo "❌ Тест провален: программа не завершилась с ошибкой при отсутствии аргументов"
    exit 1
else
    echo "✅ Тест пройден: правильная обработка отсутствия аргументов"
fi 

echo "🧪 Тест 2: Проверка с лишними аргументами"
if ./DO 1 2; then
    echo "❌ Тест провален: программа не завершилась с ошибкой при лишних аргументах"
    exit 1
else
    echo "✅ Тест пройден: правильная обработка лишних аргументов"
fi

echo "🧪 Тест 3: Проверка валидного аргумента"
if ! ./DO 1; then
    echo "❌ Тест провален: ошибка при корректном аргументе"
    exit 1
else
    echo "✅ Тест пройден: программа работает с правильным аргументом"
fi

echo "🧪 Тест 4: Проверка невалидного аргумента"
if ./DO 7; then
    echo "❌ Тест провален: программа не завершилась с ошибкой при невалидном аргументе"
    exit 1
else
    echo "✅ Тест пройден: правильная обработка невалидного аргумента"
fi

echo "🎉 Все интеграционные тесты успешно пройдены!"
exit 0